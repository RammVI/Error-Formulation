% Master element data generation library
%
% This library builds master or reference element information for use
% in finite element methods.
%
% The available routines are as follows:
%
%      MASTR  Define master element and master element basis function info
%      P1     defines values of basis functions on a master element for P1
%      P1bub  defines values of basis functions on a master element for P1bub
%      P2     defines values of basis functions on a master element for P2
%
