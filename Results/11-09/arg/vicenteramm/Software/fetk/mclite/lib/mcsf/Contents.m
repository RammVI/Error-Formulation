% MCSF mesh library
%
% This library contains a number of simplex meshes in
% MCSF (=MC Simplex Format) format, contributed by various individuals.
%
% The available meshes are as follows:
%
%      ABAR          a bar
%      CUBE          a cube
%      SQUARE        a square
%      TORUS         a torus
%      TORUS2        a second torus
%      ONEHOLE       a one-hole mesh
%      TRUSS         a truss mesh
%      TRUSS2        a second truss mesh
%      TWOHOLES      a two-hole mesh
%
% Author:   Michael Holst
% rcsid="$Id: Contents.m,v 1.1.1.1 2007/04/27 08:28:08 hrg Exp $"
