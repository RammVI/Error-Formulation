% Utility library
%
% This library contains a number of useful utilities for use by MCLite and
% other MATLAB codes.
%
% The available routines are as follows:
%
%      ASSERTW       assertion macro for debugging
%      D             move the plot down
%      L             move the plot left
%      MCLITEHI      MCLite copyleft run-time blurb
%      MYEIG         simple QR iteration
%      MYMOD         simple modulus operation
%      MYREM         simple remainder operation
%      MYPAUS        pause until the return key is pressed
%      R             move the plot right
%      U             move the plot up
%      ZEDGMESH      builds MCEF mesh by treating matrix as graph
%
% Author:   Michael Holst
% rcsid="$Id: Contents.m,v 1.1.1.1 2007/04/27 08:28:19 hrg Exp $"
